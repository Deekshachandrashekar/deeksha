package nestedloops;

public class dollor {
	public static void main(String[]args)
	{
		for (int i=1; i<=5; i++)
		{
			for(int k=1; k<=1; k++)
			{
				System.out.println("*");
			}
			for(int j=1; j<=i; j++)
			{
				System.out.println("$");
			}
			System.out.println();
			
			}
	}
}
		
	


